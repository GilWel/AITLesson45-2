package Homework13_1604;

import java.util.Objects;

public class CalculatorUtils {

    public static double averageForThird(double first, double second, double third, int n) {
        return (first + second + third) / n;
    }
    public static double averageForSecond(double first, double second, int n) {

        return (first + second) / n;
    }
}
